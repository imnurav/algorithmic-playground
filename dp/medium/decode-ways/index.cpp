#include <iostream>
using namespace std;
int numDecodings(string s)
{
    int n = s.length();
    if (s[0] == '0')
        return 0;

    vector<int> dp(n + 1, 0);
    dp[0] = 1; // empty string
    dp[1] = 1; // first character is valid

    for (int i = 2; i <= n; i++)
    {
        if (s[i - 1] != '0')
            dp[i] += dp[i - 1];
        int twoDigit = (s[i - 2] - '0') * 10 + (s[i - 1] - '0');
        if (twoDigit >= 10 && twoDigit <= 26)
        {
            dp[i] += dp[i - 2];
        }
    }

    return dp[n];
}
int main()
{
    string s = "12";
    cout << numDecodings(s);
    return 0;
}